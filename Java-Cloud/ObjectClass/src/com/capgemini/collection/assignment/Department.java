package com.capgemini.collection.assignment;

import java.util.LinkedHashSet;

public class Department {
	int id;
	String dept;
	
	public Department(int id, String dept) {
		super();
		this.id = id;
		this.dept = dept;
	}
	
	@Override
	public String toString() {
		return "LinkedHashSet1 [id=" + id + ", dept=" + dept + "]";
	}
	
	

}

	private Integer usrAge;
	private String usrPassword;
	private long usrPhone;
	public String getUsrName() {
		return usrName;
	}
	public void setUsrName(String usrName) {
		this.usrName = usrName;
	}
	public Integer getUsrAge() {
		return usrAge;
	}
